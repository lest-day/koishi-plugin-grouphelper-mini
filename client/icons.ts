/**
 * GitHub Octicons 图标管理器
 * @example
 * // 注册新图标
 * Octicons.register('star', 'M8 .25a.75.75 0 0 1 .673.418l1.882...')
 * Octicons.register('repo', '...', 24) // 24x24 图标
 *
 * // 使用图标 (在模板中)
 * <k-icon name="grouphelper:octicons.star" />
 */
export const Octicons = {
  /** 存储所有 Octicons 路径 */
  paths: {} as Record<string, { d: string; size: 16 | 24 }>,

  /**
   * 注册 GitHub Octicons 图标
   * @param name 图标名称
   * @param pathData SVG path 的 d 属性
   * @param size 图标尺寸，默认 16
   */
  register(name: string, pathData: string, size: 16 | 24 = 16) {
    this.paths[name] = { d: pathData, size }
  },

  /**
   * 批量注册图标
   * @param icons 图标配置对象
   */
  registerAll(icons: Record<string, string | { d: string; size?: 16 | 24 }>) {
    for (const [name, value] of Object.entries(icons)) {
      if (typeof value === 'string') {
        this.register(name, value)
      } else {
        this.register(name, value.d, value.size ?? 16)
      }
    }
  },

  /**
   * 创建图标组件
   * @param name 已注册的图标名称
   */
  create(name: string) {
    const icon = this.paths[name]
    if (!icon) {
      console.warn(`[Octicons] Icon "${name}" not found`)
      return () => h('svg', {})
    }
    return () => h('svg', {
      viewBox: `0 0 ${icon.size} ${icon.size}`,
      fill: 'currentColor',
      width: '1em',
      height: '1em'
    }, [h('path', { d: icon.d })])
  },

  /**
   * 获取所有已注册的图标组件
   */
  getAll() {
    const result: Record<string, ReturnType<typeof this.create>> = {}
    for (const name of Object.keys(this.paths)) {
      result[name] = this.create(name)
    }
    return result
  }
}

